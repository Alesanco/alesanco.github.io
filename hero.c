#include <stdio.h>

#define SUCCESS 0
#define FAILURE 1

unsigned char s[] = 
{
  0xfd, 0x9b, 0x9e, 0x5a, 0x90, 0x8a, 0xdf, 0xde, 
  0x4d, 0x4e, 0x33, 0x74, 0x24, 0x72, 0xe0, 0xa8, 
  0xce, 0x57, 0x79, 0xda, 0x3d, 0xd9, 0xf8, 0x7, 
  0xb1, 0xd7, 0x6c, 0x93, 0x36, 0xb1, 0x2d, 0xd3, 
  0x46, 0x8d, 0x54, 0xfb, 0x5, 0x97
};

int print_hero_message() {
  for (unsigned int m = 0; m < sizeof(s); ++m)
  {
    unsigned char c = s[m];
    c ^= 0x77;
    c = -c;
    c ^= m;
    c += 0x43;
    c = ~c;
    c += 0x1c;
    c ^= 0x8;
    c = (c >> 0x5) | (c << 0x3);
    c ^= 0x5;
    c = ~c;
    c -= m;
    c = (c >> 0x6) | (c << 0x2);
    c ^= m;
    c = ~c;
    c -= m;
    s[m] = c;
  }

  printf("\n%s\n", s);

  return SUCCESS;
}
